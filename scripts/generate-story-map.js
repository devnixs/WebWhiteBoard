#!/usr/bin/env node

const fs = require("fs");
const path = require("path");

const root = process.cwd();
const storiesDir = path.join(root, "stories");
const storyMapPath = path.join(storiesDir, "00-story-map.md");
const storyFilePattern =
  /^(\d{2})-[a-z0-9]+(?:-[a-z0-9]+)*\.story_(TODO|IN_PROGRESS|COMPLETE)\.md$/;

function rel(filePath) {
  return path.relative(root, filePath);
}

function listMarkdownFilesRecursive(dir) {
  if (!fs.existsSync(dir)) {
    return [];
  }

  const files = [];
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...listMarkdownFilesRecursive(fullPath));
    } else if (entry.isFile() && entry.name.endsWith(".md")) {
      files.push(fullPath);
    }
  }

  return files.sort();
}

function storySortKey(storyPath) {
  const match = storyFilePattern.exec(path.basename(storyPath));
  return match ? Number(match[1]) : Number.MAX_SAFE_INTEGER;
}

function collectStories() {
  const stories = listMarkdownFilesRecursive(storiesDir).filter((file) => {
    const name = path.basename(file);
    return (
      name !== "00-story-map.md" &&
      name !== "README.md" &&
      !name.startsWith("_") &&
      storyFilePattern.test(name)
    );
  });

  return stories.sort((a, b) => {
    const numberDelta = storySortKey(a) - storySortKey(b);
    return numberDelta || rel(a).localeCompare(rel(b));
  });
}

function formatStoryList(stories) {
  if (stories.length === 0) {
    return "- None\n";
  }

  return stories.map((story) => `- \`${rel(story)}\``).join("\n") + "\n";
}

function main() {
  const stories = collectStories();
  const activeStories = stories.filter(
    (story) => !path.basename(story).includes("_COMPLETE")
  );
  const completedStories = stories.filter((story) =>
    path.basename(story).includes("_COMPLETE")
  );

  const content = `# Story Map

This file is generated by \`npm run generate:story-map\`. Do not edit it by hand.

## Completion Rule

- Every story in this folder is complete only when the implementation is done and the relevant checklist items in the mapped \`specs/*.spec.md\` files have been updated from \`- [ ]\` to \`- [X]\`.
- If implementation changes behavior, update the relevant spec items first or in the same change set.
- Run \`npm run validate:workflow\` before closing a story.

## Active Stories

${formatStoryList(activeStories)}
## Archived Completed Stories

${formatStoryList(completedStories)}`;

  fs.writeFileSync(storyMapPath, content);
  console.log(`Generated ${rel(storyMapPath)} with ${stories.length} stories.`);
}

main();
